#include "libft.h"

char	*ft_strjoin(char *s1, char *s2)
{
	char	*j;
	int		i;
	int		g;

	i = 0;
	g = 0;
	j = (char*)malloc(sizeof(char*));
	while (s1[i])
	{
		j[i] = s1[i];
		i++;
	}
	while (s2[g] != '\0')
	{
		j[i] = s2[g];
		i++;
		g++;
	}
	j[i] = '\0';
	return (j);
}
