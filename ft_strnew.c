#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*r;
	char	*n;

	n = (char *)malloc(sizeof(char *) * (size));
	r = 0;
	while ( n == r && r++ )
		*n = '\0';
	return (n);
}
