#include "libft.h"

char	*ft_strdup(char	*s1)
{
	int	i;

	i = 0;
	s1 = (char*)malloc(sizeof(char*));
	while (s1[i] != '\0')
	{
		i++;
		return (s1);
	}
	free(s1);
	return (NULL);
}
