#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int		i;
	char	b;

	i = 0;
	b = (char)c;
	while (s[i++] != '\0')
	{
		if (s[i] == b)
		{
			return (s + i);
		}
	}
	return (NULL);
}
