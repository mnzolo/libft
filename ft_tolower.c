#include <ctype.h>
#include <unistd.h>

void	ft_putchar(char c)
{
	write(1,&c,1);
}

char		ft_tolower(int c)
{
	if(c >= 'A' && c <= 'Z')
	{
		c = c + 32;
	}
	return(c);
}
